function changeDetails(indexProduct)
{
	var prod;

	products.forEach(function(product)
		{
			if(product.index == indexProduct)
				prod = product;
		}
	);

	if(prod)
	{
		changeHtml("descriptionTitle", prod.title);
		changeIcon("imgDetail", prod.src);
		changeHtml("descriptionContent", prod.description);
		$('#containerDetails').goTo();
	}
}

function changeIcon(idImg,srcImage)
{
	var domImg = document.getElementById(idImg);
    var img = new Image();
    img.onload = function()
    {
        // Load completed
        domImg.src = this.src;
    };
    img.src = srcImage;
}

function changeHtml(idItem, newHtml)
{
	document.getElementById(idItem).innerHTML = newHtml;
}


(function($) {
    $.fn.goTo = function() {
        $('html, body').animate({
            scrollTop: $(this).offset().top + 'px'
        }, 'fast');
        return this; // for chaining...
    }
})(jQuery);